from scipy.optimize import linprog

# Коэффициенты целевой функции (минимизация затрат)
c = [-75, -82]

# Коэффициенты ограничений (левые части)
A = [
    [0.2, 0.2],
    [0.3, 0.4],
    [3, 4]
]

# Правые части ограничений
b = [150, 280, 2100]

# Границы переменных (неотрицательные значения)
x_bounds = (0, None)
y_bounds = (0, None)

# Решение задачи линейного программирования
result = linprog(c, A_ub=A, b_ub=b, bounds=[x_bounds, y_bounds], method='highs')

# Оптимальные значения переменных
x_optimal = result.x[0]
y_optimal = result.x[1]

# Максимальное значение целевой функции (максимизация прибыли)
max_profit = -result.fun  # Инвертируем знак, так как linprog минимизирует

print("Оптимальное количество молочного мороженого:", x_optimal, "кг")
print("Оптимальное количество сливочного мороженого:", y_optimal, "кг")
print("Максимальная прибыль:", max_profit, "руб.")

