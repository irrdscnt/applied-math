from scipy.optimize import linprog

# Целевая функция: максимизация дохода Z = 800x + 720y (отрицательный коэффициент для минимизации -800x - 720y)
c = [-800, -720]

# Коэффициенты ограничений (левая часть системы ограничений Ax <= b)
A = [
    [3, 2.4],  # Ограничение по рабочему времени: 3x + 2.4y <= 900
    [1, 0],    # Ограничение по максимальному количеству валов: x <= 250
    [0, 1]     # Ограничение по максимальному количеству втулок: y <= 340
]

# Правая часть системы ограничений Ax <= b
b = [900, 250, 340]

# Определение границ переменных (x и y должны быть неотрицательными)
x_bounds = (0, None)
y_bounds = (0, None)

# Решение задачи ЛП
result = linprog(c, A_ub=A, b_ub=b, bounds=[x_bounds, y_bounds], method='highs')

if result.success:
    optimal_x = result.x[0]  # Количество произведенных валов (x)
    optimal_y = result.x[1]  # Количество произведенных втулок (y)
    optimal_profit = -result.fun  # Максимальный доход (уберем отрицательный знак)

    print("Оптимальное количество валов (x):", optimal_x)
    print("Оптимальное количество втулок (y):", optimal_y)
    print("Максимальный доход:", optimal_profit, "рублей")
else:
    print("Задача не имеет оптимального решения.")

